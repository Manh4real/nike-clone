export function getProduct(productId) {
  return data.find((p) => p.id === productId);
}

export function getProducts(productIds) {
  return data.filter((p) => productIds.includes(p.id));
}

export function getFromLocal(type) {
  const c = JSON.parse(localStorage.getItem(type));

  if (!c) return null;

  const r = getProducts(c.map((p) => p.id));
  const rs = c.map((p) => {
    const x = r.find((y) => y.id === p.id);
    return { ...p, ...x };
  });

  return rs;
}
